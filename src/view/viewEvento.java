    private void buttonNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonNovoActionPerformed
         evento e = new evento();
         System.out.println("teste");
        
        if(String.valueOf(textIDCardapio.getText()).isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione um cardápio");
            return;
        }
        if(textNome.equals("")){
            JOptionPane.showMessageDialog(null, "Verifique os dados Informados");
            return;
        }
        
        eventoController ec = new eventoController();
        e.setNomeEvento(textNome.getText());
        e.setIdCardapio(Integer.parseInt(textIDCardapio.getText()));
        e.setStatus(true);
        ec.cadastrar(e);

        this.atualizaTabela();
        this.limparTela();
    }//GEN-LAST:event_buttonNovoActionPerformed

    private void tabelaEventoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaEventoMouseClicked
        tabelaEvento.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent arg0) {
                    int linha = tabelaEvento.getSelectedRow();
                    textID.setText(tabelaEvento.getValueAt(linha, 0).toString());
                    textNome.setText(tabelaEvento.getValueAt(linha, 1).toString());
                    textIDCardapio.setText(tabelaEvento.getValueAt(linha, 2).toString());
                    String marcado = tabelaEvento.getValueAt(linha, 3).toString();
                    if(marcado=="true"){
                        checkAtivo.setSelected(true);
                    }
            }
        });
    }//GEN-LAST:event_tabelaEventoMouseClicked


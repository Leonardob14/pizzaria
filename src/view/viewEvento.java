    private void buttonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditarActionPerformed
            evento e = new evento();

            if(String.valueOf(textID.getText()).isEmpty()){
                JOptionPane.showMessageDialog(null, "Verifique os dados Informados");
                return;
            }
            if(String.valueOf(textIDCardapio.getText()).isEmpty()){
                JOptionPane.showMessageDialog(null, "Verifique os dados Informados");
                return;
            }
            if(String.valueOf(textNome.getText()).isEmpty()){
                JOptionPane.showMessageDialog(null, "Verifique os dados Informados");
                return;
            }
            eventoController ec = new eventoController();
            e.setId(Integer.parseInt(textID.getText()));
            e.setNomeEvento(textNome.getText());
            e.setIdCardapio(Integer.parseInt(textIDCardapio.getText()));
            e.setStatus(false);
            if(checkAtivo.isSelected()){
                e.setStatus(true);
            }
            ec.alterar(e);

            this.atualizaTabela();
            this.limparTela();
    }//GEN-LAST:event_buttonEditarActionPerformed

    private void buttonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarActionPerformed
            eventoController ec = new eventoController();
            evento e = new evento();
            e.setNomeEvento(textNome.getText());
            e.setStatus(false);
            if(checkAtivo.isSelected()){
                e.setStatus(true);
            }
            this.atualizarBusca(e);
    }//GEN-LAST:event_buttonBuscarActionPerformed

    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        eventoController ec = new eventoController();
        evento e = new evento();
        
        if(String.valueOf(textID.getText()).isEmpty()){
            JOptionPane.showMessageDialog(null, "ID não pode ser vazio");
            return;
        }
        if(String.valueOf(textIDCardapio.getText()).isEmpty()){
            JOptionPane.showMessageDialog(null, "Cardápio não pode ser vazio");
            return;
        }
        if(String.valueOf(textNome.getText()).isEmpty()){
            JOptionPane.showMessageDialog(null, "Nome não pode ser vazio");
            return;
        }
        
        e.setId(Integer.parseInt(textID.getText()));
        ec.excluir(e);
        this.limparTela();
        this.atualizaTabela();
    }//GEN-LAST:event_buttonExcluirActionPerformed


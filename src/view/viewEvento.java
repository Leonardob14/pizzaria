    public void atualizaTabela(){
        cardapioController cc = new cardapioController();
        eventoController ec = new eventoController();
        tabelaEvento.setModel(new eventoTableModel(ec.listarTodos()));
        tabelaCardapio.setModel(new cardapioTableModel(cc.listarTodos()));
    }
    public void atualizarBusca(evento e){
        eventoController ec = new eventoController();
        tabelaEvento.setModel(new eventoTableModel(ec.buscar(e)));
    }
    
    public void limparTela(){
        textID.setText("");
        textNome.setText("");
        textIDCardapio.setText("");
        checkAtivo.setSelected(false);
    }

